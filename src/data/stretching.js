// Helper to build a routine stretch entry with library reference
function s(key, duration, noteOverride) {
  const lib = STRETCH_LIBRARY[key];
  return {
    key,
    name: lib.name,
    duration,
    note: noteOverride || lib.variants[1]?.note || lib.variants[0].note,
    muscles: lib.muscles,
    details: lib.details,
    variants: lib.variants,
  };
}

